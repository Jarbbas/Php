    $number = 34;

    switch($number){
        case 34:
            echo "is this 34";
            break;
        case 37:
            echo "is this 34";
            break;
        case 40:
            echo "is this 34";
            break;
        case 20:
            echo "is this 34";
            break;
        default:
            break;
    }
    
    
